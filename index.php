				<h3>Bazooka</h3>
				<p class="lead">Ca armă antitanc portabil americanii foloseau bazooka (descoperit în 1942), iar germanii panzerschreck și panzerfaust (de unică folosință, construit din 1943) cu grenadă reactivă antitanc.</p>
				<h3>Mortiere</h3>
				<p class="lead">Mortierele folosite erau împărțite în două mari calibre: de 50mm (de ex. mortierul japonez Tip 89) și 80 mm (de ex. mortierul american M1).</p>
				<h3>Bomba atomică</h3>
				<p class="lead">Antisemitismul german a declanșat o masivă emigrare a oamenilor de știință evrei din Europa ocupată de germani în SUA. Aceștia au format miezul cercetătorilor care în cadrul Proiectului Manhattan au dezvoltat cu succes bomba atomică. Bomba a fost destinată bombardării Germaniei, dar Germania Nazistă a capitulat înainte ca bomba să fie finalizată. Bomba atomică a fost în cele din urmă folosită împotriva japonezilor și a obligat Japonia Imperială să capituleze, salvând astfel multe vieți americane, care ar fi pierit în timpul debarcării aliaților în Japonia unde se preconiza că fanatismul japonezilor va fi extrem. După război, existența bombei atomice în Războiul Rece ce a urmat a declanșat cursa înarmării.</p>
				<h3>Nave</h3>
				<p class="lead">S-au făcut progrese aproape în toate aspectele războiului maritim, dar progresele cele mai mari s-au înregistrat în domeniul portavioanelor și submarinelor.<br>
					Războiul naval s-a schimbat în mod dramatic în timpul celui de-al Doilea Război Mondial, cu ascensiunea portavioanelor în defavoarea cuirasatelor, care s-au dovedit vulnerabile la atacurile avioanelor. Au fost scufundate cu bombe cuirasate celebre construite pentru sume exorbitante, cum ar fi de exemplu cuirasatul britanic Hood, cuirasatul german Bismarck sau cuirasatul japonez Yamato.<br>
				Astfel, după luptele navale de la Pearl Harbor, Marea Chinei și din Marea Coralilor în lume s-a impus portavionul ca navă principală.</p>
				<h3>Submarine</h3>
				<p class="lead">Submarinele au avut de asemenea impact major pe parcursul războiului. Dat fiind puterea flotei britanice, pierderea cuirasatului Bismarck au determinat germanii să întreprindă eforturi în dezvoltarea flotei lor de submarine în defavoarea cuirasatelor. Noile tipuri de submarine germane au intrat în serviciu prea târziu și după ce aproape toate echipajele cu experiență au pierit.
					<br>
					Submarinele germane (U-boot) au fost utilizate în principal pentru oprirea / distrugerea ajutorului american și canadian peste Atlantic. În prima perioadă a războiului submarinele germane au repurtat mari succese, dar după ce Aliații au dezvoltat tactici eficiente împotriva lor, pierderile submarinelor erau tot mai mari cu scăderea drastică a numărului de nave aliate scufundate.
					<br>
					Submarinele americane acționau eficient împotriva proviziilor japoneze de petrol și alimente, care erau îndreptate spre arhipelagul japonez, astfel că cea mai mare parte din flota comercială a Imperiului Japonez a fost scufundată.<br>
					Lipsa combustibililor în cantități suficiente i-a împiedicat pe japonezi să asigure piloților pregătire corespunzătoare și chiar a forțat marina militară să rămână în apropierea bazelor cu surse de combustibil. Printre navele de război care a fost scufundată de submarine a fost cel mai mare portavion din război, portavionul japonez Shinano.<br>

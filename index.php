			<h1 align="center">Urmările celui de-al Doilea Război Mondial</h1>
			<br><br>
			<h3>Europa ruinată</h3>
			<p class="lead">La sfârșitul războiului, milioane de refugiați din întreaga Europă nu mai aveau niciun fel de locuință, economia era în criză profundă, iar o bună parte a infrastructurii industriale a continentului era distrusă. Uniunea Sovietică a fost puternic afectată, aproximativ o treime din economia sa fiind distrusă.</p>
			<p class="lead">Raidurile executate de Luftwaffe în 1939 asupra orașelor Frampol, Wieluń și Varșovia au instituit practica bombardării țintelor pur civile.</p>
			<p class="lead">Regatul Unit s-a aflat la sfârșitul războiului în tabăra învingătorilor, dar economia țării era secătuită de eforturile de război. Guvernul de coaliție din timpul războiului a fost dizolvat. La primele alegeri parlamentare de după încheierea războiului, Winston Churchill a fost învins de laburiștii conduși de Clement Attlee.</p>
			<p class="lead">În 1947, Secretarul de Stat american George Marshall a lansat „Programul European de Refacere” (Planul Marshall), care s-a desfășurat în anii 1948 - 1952. SUA au alocat 13 miliarde de dolari pentru reconstrucția Europei de Vest.</p>
			<figure class="figure">
				<img class="figure-img img-fluid" src="public/img/var.jpg">
				<figcaption class="figure-caption">Varșovia distrusă de forțele germane.</figcaption>
			</figure>
			<br><br>
			<h3>Europa Centrală și Răsăriteană sub controlul comuniștilor</h3>
			<p class="lead">La sfârșitul războiului, Uniunea Sovietică ocupase cea mai mare parte a Europei Centrale și Răsăritene și a Balcanilor. În toate țările ocupate de sovetici, cu excepția Austriei, URSS-ul a acordat un ajutor activ regimurilor de stânga procomuniste, venite la putere cu ajutorul Armatei Roșii. De asemenea, la sfârșitul războiului, Uniunea Sovietică a reocupat și încorporat statele baltice.</p>
			<br><br>
			<h3>Germania</h3>
			<p class="lead">Germania a fost împărțită în patru zone de ocupație, coordonate de Consiliul Aliat de Control. Zonele americană, britanică și franceză au fost unite în 1949 pentru a forma Republica Federală Germania, iar zona sovietică a devenit Republica Democrată Germană. În Germania de vest au fost concepute mai multe planuri pentru dezvoltare economică și s-a desfășurat o activitate de denazificare, care s-a întins pe mai mulți ani.</p>
			<p class="lead">Milioane de germani și de polonezi au fost strămutați de pe pământurile natale ca rezultat direct al anexărilor care au avut loc în Europa Răsăriteană, în conformitate cu înțelegerile stabilite la conferințele de la Ialta și Potsdam. Există autori care afirmă că între 1 și 2 milioane de germani au pierit în timpul strămutărilor forțate în vest. Alsacia și Lorena au fost retrocedate Franței, iar regiunea Saar a fost separată de Germania și a format o uniune economică cu Franța.</p>
			<p class="lead">Efectele marelui război se resimt în Germania și la începutul secolului XXI prin exploziile care au loc datorită unor bombe rămase neexplodate.</p>
			<figure class="figure">
				<img class="figure-img img-fluid" src="public/img/Vertreibung.jpg">
				<figcaption class="figure-caption">Expulzările germanilor din regiunea Sudeților</figcaption>
			</figure>
			<br><br>
			<h3>Austria</h3>
			<p class="lead">Republica Federală a Austriei (Bundesstaat Österreich), așa cum s-a numit începând cu 1934, fusese anexată de Germania nazistă în 1938 prin Anschluss, încălcându-se Tratatul de la Versailles. După încheierea războiului, Austria a fost separată de Germania și a fost divizată în patru zone de ocupație, care au fost reunite în 1955, prin Tratatul de independență a Austriei pentru a deveni Republica Austria așa cum o știm în zilele noastre.</p>
			<br><br>
			<h3>Ocuparea Japoniei și Coreii</h3>
			<p class="lead">Japonia a fost ocupată de trupele SUA în alianță cu cele ale Commonwealth până la intrarea în funcțiune a prevederilor tratatului de pace în 1952. În conformitate cu înțelegerile Conferinței de la Ialta, Uniunea Sovietică a ocupat și anexat în cele din urmă Insula Sahalin. Au fost înființate „Comisia Orientului Îndepărtat” și „Comisia Aliată de Control” pentru Japonia, cu funcții similare cu ale Comisiei Aliat de Control pentru Germania.[2] În timpul ocupației, americanii s-au concentrat pe demilitarizarea națiunii nipone, desființarea industriei militare japoneze și organizarea unei vieți politice democratice, bazată pe o nouă constituție. Mulți istorici consideră perioada de ocupație a Japoniei ca fiind una încununată de un mare succes economic și social. Perioada de ocupație s-a încheiat în 1952, fiind urmată imediat de un boom economic al Japoniei.</p>
			<p class="lead">Coreea a fost împărțită între URSS și SUA, ceea ce a dus la proclamarea în 1948 a două guverne separate. Sub auspiciile sovietice, partea nordică a penininsulei și-a proclamat independența ca Republica Populară Democrată Coreeană, în timp ce regimul anticomunist sud-coreean sprjiinit de americani și-a proclamat îndependența ca Republica Coreea. Aceste două regimuri s-au angajat în primul conflict „fierbinte” al războiului rece – Războiul din Coreea (1950-1953). Acest război a fost prima implicare a armatei americane în acțiuni ofensive după al doilea război mondial și primul test de funcționare a nou formatei organizații mondiale ONU. Peninsula Coreeană este divizată în continuare și în zilele noastre.</p>
			<br><br>
			<h3>Sfârșitul colonialismului european</h3>
			<p class="lead">Teritoriile ocupate până la război de puterile coloniale și-au căpătat treptat independența. Dacă în cazul unora dintre ele procesul de obținere a independenței a fost unul pașnic, așa cum a fost cazurile Filipinelor în 1946 și a Indiei și Pakistanului în 1947, în cazul altor națiuni acestea au trebuit să ducă lupte sângeroase pentru eliberare, ca în cazul Vietnamului, care s-a luptat împotriva încercărilor francezilor de recucerire a țării, sau în cazul Indiilor Răsăritene Olandeze. Japonia reușise să sădească în rândul națiunilor sud-asiatice un sentiment naționalist, care s-a dezvolatat în special în Filipine, Vietnam și Cambodgia. Perioada postbelică a marcat sfârșitul caracterului imperialist al marilor puteri europene.</p>
			<br><br>
			<h3>China</h3>
			<p class="lead">Al doilea război mondial a fost un punct de cotitură în istoria Chinei. Mai înainte de războiul mondial, China suferise timp de aproape o sută de ani umiliri din parte puterilor coloniale europene și ajunsese în starea de semicolonie. Lupta împotriva japonezilor din cel de-al doilea război mondial a ridicat enorm statutul internațional al Chinei. Guvernul central al lui Chiang Kai-shek a reușit să abroge majoritatea tratatelor inechitabile pe care fusese silită China să le semneze de-a lungul ultimului secol, iar Republica China a devenit unul dintre membrii fondatori ai ONU și membru permanent al Consiliului de Securitate. China a revendicat Manciuria și Taiwanul. Totuși, cei opt ani de război au adus mari prejudicii guvernului central, iar numeroase dintre măsurile de unificare, adoptate de când venise la putere în 1928, au fost distruse de război. Activitățile comuniștilor s-au extins foarte mult în zonele în care luptaseră, ceea ce a făcut ca măsurile administrației centrale să fie greu de aplicat. Distrugerile provocate de război și hiperinflația au demoralizat populația, iar continuarea războiului civil dintre Kuomintang și comuniști a aruncat țara în haos. În parte datorită pierderilor grele suferite în al doilea război mondial, în parte datorită înfrângerilor suferite în fața comuniștilor, Kuomintangul și aparatul de stat al Republicii China s-au retras în Taiwan în 1949. În China continentală, Partidul Comunist Chinez a proclamat Republica Populară Chineză.</p>
			<br><br>
			<h3>Schimbările de frontieră: Germania, Polonia, România și Uniunea Sovietică</h3>
			<p class="lead">Ca urmare a noilor frontiere desenate de națiunile victorioase, numeroase persoane s-au trezit în teritorii ostile. Uniunea Sovietică a ocupat teritoriile care au aparțiunt până în acel moment Germaniei, Finlandei, Poloniei, Japoniei și României. Polonia a primit cea mai mare parte a teritoriilor din răsărit ale Germanie la est de linia Oder-Neisse, inclusiv regiunea industrială a Sileziei. Regiunea germana Saar a fost trecuta temporar sub protectoratul Franței, dar a fost retrocedata Germaniei în cele din urmă. Numărul germanilor expulzați, (după cum se stabilise de altfel la Conferința de la Potsdam), a fost de aproximativ 15 milioane de oameni, din care cam 11 milioane din fostele teritoriii germane și cam 3,5 milioane din zona Sudeților. Se estimează că în cursul procesulului de expulzare – strămutare, între 1 și 2 milioane de germani și-au pierdut viața.</p>
			<p class="lead">În Europa Răsăriteană, 4 milioane de polonezi au fost expulzați de Uniunea Sovietică din regiunea de răsărit a Poloniei care a fost cedată URSS, care se afla cu aproximație la est de Linia Curzon. Aceste schimbări teritoriale au schimbat practic definitiv rezultatelel războiului polono-sovietic din 1919-1920. Foste orașe poloneze precum L'vovul au trecut sub controlul URSS și al Ucrainei sovietice.</p>
			<p class="lead">Basarabia și Bucovina de nord, părți ale Regatului României, au fost trecute definitiv în componența URSS-ului. În cea mai mare parte a Basarabiei a fost proclamată RSS Moldovenească, iar Bucovina de nord și Bugeacul au trecut în componența Ucrainei. În teritoriile românești ocupate de URSS s-a trecut la sovietizarea și rusificarea populației băștinașe.</p>
			<br><br>
			<h3>Reparațiile de război și ocupația aliată</h3>
			<p class="lead">Germania a plătit reparații de război către Regatul Unit, Franța și Uniunea Sovietică, în principal prin demontarea și exportarea unor fabrici întregi, dar și prin exportul de cărbune și munca forțată a unor germani. Standardul de viață din Germania a fost redus la cel din timpul Marii crize economice. Aproape imediat după capitularea Germaniei și în următorii doi ani, SUA a declanșat un proces intensiv de furt de descoperiri științifice și tehnice și de recrutare de specialiști din diferite domenii. În cartea Science Technology and Reparations: Exploitation and Plunder in Postwar Germany a lui John Gimbel, autorul ajunge la concluzia că „reparațiile intelectuale” luate de americani și britanici ar putea fi echivalate cu aproximativ 10 miliarde de dolari la cursul anului 1945, adică aproximativ 100 miliarde de dolari din zilele noastre.[4]Programul american de recrutare a oamenilor de știință și a tehnicienilor germani era îndreptat și împotriva Uniunii Sovietice, care nu trebuia să pună mâna pe descoperirile tehnico-științifice și pe savanții germani.</p>
			<br><br>
			<h3>ONU</h3>
			<p class="lead">Datorită faptului că Liga Națiunilor a eșuat în tentativa de prevenire a războiului, a fost creată în 1945 Organizația Națiunilor Unite. ONU funcționează pe baza Cartei Națiunilor Unite. Una dintre primele acțiuni ale ONU a fost crearea statului Israel.</p>
			<h3>Statele Unite</h3>
			<p class="lead">
				Statele Unite au devenit cea mai puternică națiune a lumii, secondată îndeaproape de URSS. SUA au beneficiat din punct de vedere militar, financiar și politic de pe urma războiului, strângând legăturile cu statele europene și în principal cu Regatul Unit. SUA și-au stabilit un puternic punct de sprijin în Europa Occidentală și în Germania de Vest și a îndiguit înaintarea noului său inamic, blocul răsăritean.
			</p>
			<h3>Războiul rece</h3>
			<p class="lead">Odată cu explozia primei bombe nucleare, SUA au avut pentru o vreme monopolul armamentului nuclear. URSS a fost urmatoarea putere nucleară care și-a cucerit acest statut în 1949. S-a declanșat războiul rece și cursa înarmărilor nucleare, care au dominat ultimele decenii ale secolului al XX-lea și își mai fac simțită și azi prezența.</p>

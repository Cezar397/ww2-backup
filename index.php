				<h1 align="center">Tehnologii</h1>
				<br><br>
				<p class="lead">
				Tehnologia a jucat un rol crucial în rezultatului celui de-al Doilea Război Mondial. O mare parte din aceste tehnologii au fost dezvoltate în anii interbelici: 1920 - 1930, unele s-au dezvoltat ca urmare a lecțiilor valoroase învățate în timpul războiului, iar altele spre sfârșitul războiului.</p>
				<p class="lead">
				Efectele noilor tehnologii au fost multiple și individuale, dezvoltările cele mai importante au fost în următoarele domenii:</p>
				<ul style="font-size: 19px;">
					<li><b>Armament</b>, incluzând aici: nave, vehicule, avioane, rachete, piese de artilerie, arme de infanterie și arma nucleară.</li>
					<li><b>Logistică militară</b>, incluzând aici: vehicule pentru transportul soldaților și echipamentului (camioane, jeep-uri, nave, avioane).</li>
					<li><b>Comunicații, spionaj și contraspionaj</b>, incluzând aici: echipamente de comunicații, navigație, detectare la distanță și spionaj.</li>
					<li><b>Medicină</b>, dezvoltarea unor tehnici chirurgicale, descoperirea unor medicamente.</li>
				</ul>
				<p class="lead">
					În timpul celui de-al Doilea Război Mondial armamentul a cunoscut o dezvoltare rapidă astfel că în decurs de șase ani începând de la arme cu calibre mici până la avioane armele cu care forțele au început războiul au fost depășite. De exemplu, dacă tranșeele, cavaleria și cuirasatele erau larg folosite în 1940, peste șase ani armele moderne erau avioanele cu reacție, rachetele balistice și chiar bomba atomică.
				</p>
				<br><br>
				<h2>Avioane</h2>
				<br>
				<p class="lead">
					Forțele Axei aveau probleme serioase din cauza lipsei de combustibil petrolier. Germania a dezvoltat un procedeu tehnologic prin care se obținea un înlocuitor al petrolului din cărbune. Aliații aveau producție petrolieră mult mai mare. Americanii adăugau tetraetil de plumb combustibililor folosite pentru avioane, astfel se obțineau pentru avioanele aliaților un combustibil care permitea o compresie mai mare a combustibilului, randament mai mare, crescând astfel viteza și raza de acțiune a avioanelor.<br>
					Forțele aeriene franceze erau foarte neglijate, comandanții militari preferând să cheltuiască fonduri pentru forțe terestre și fortificații.<br>
					Pe Teatrul de război din Europa de Vest aviația a devenit crucială de-a lungul războiului, atât în operațiuni tactice (pe câmpul de luptă) cât și cele strategice (acțiuni la mare distanță-bombardamente). Introducerea unor inovații, invenții și noi tehnologii au permis germanilor dezvoltarea în 1940 a unor avioane care depășeau în viteză avioanele aliaților, la care se adăuga și numărul insuficient de avioanele aliate.<br>
					Ca urmare, în anul 1940, Forțele Aeriene Franceze aveau doar 1562 avioane, care, împreună cu 1070 avioane ale RAF înfruntau cele 5638 avioane de vânătoare și vânătoare-bombardament ale Luftwaffe.<br>
					Cele mai multe aerodromuri franceze s-au situat în nord-estul Franței, și au fost rapid ocupate în primele etape ale campaniei. Royal Air Force din Marea Britanie poseda unele avioane de luptă foarte avansate, cum ar fi Spitfire și Hurricane, dar acestea nu au fost potrivite pentru a ataca trupele terestre pe un câmp de luptă, față de Luftwaffe, care folosea cu succes în acest scop avioanele de bombardament în picaj Stuka testate de Legiunea Condor în Războiul Civil Spaniol.<br>
					Numărul mic de avioane expediate în Franța cu Forța Expediționară Britanică au fost distruse destul de repede. Ca urmare, Luftwaffe în 1940 a fost capabil să dobândească superioritatea aeriană deasupra Franței, oferind armatei germane un avantaj imens în sprijinul trupelor terestre, recunoaștere și culegerea informațiilor.<br>
				</p>
				<p class="lead">
					În calitate de consilieri ai guvernului, cercetătorii de radiații au fost solicitați să asiste comandanții militari, care au solicitat experimentarea umană pentru a determina efectele armelor atomice asupra corpurilor lor. Dar acești cercetători au știut, de asemenea, că experimentarea umană nu poate oferi cu ușurință răspunsurile de care aveau nevoie militarii.
				</p>
				

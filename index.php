				<h3>Mitraliere și pistoale mitralieră</h3>
				<p class="lead">Ideea dezvoltării acestor arme de calibru mai mic dar cu mare putere de foc era ca trăgătorul să le poată folosi fără ajutor și în mers, astfel încât să poată avansa spre poziția inamicului acoperind inamicul cu o ploaie de plumb. Germanii au dezvoltat mai întâi FG 42 pentru parașutiști din care mai târziu a apărut Sturmgewehr 44 (StG 44), cu un cartuș intermediar, FG 42 care folosea muniție de pușcă dovedindu-se greu de controlat.
					<br>
				Evoluția din domeniul tehnologiei mitralierelor a culminat în mitraliera germană Maschinengewehr 42 (MG 42) intrată în producție în 1942, care beneficia de o proiectare de neegalat la vremea respectivă. S-a dovedit o armă fiabilă, ușor de utilizat, dar se distingea prin puterea sa de foc neegalată de până la 1500 cartușe pe minut. Această armă a stimulat dezvoltarea mitralierelor după război pe ambele părți ale Războiului Rece și este încă folosită de unele armate și în zilele noastre, inclusiv în armata germană Bundeswehr sub denumirea de MG 3. Proiectul G3 al constructorului de armament Heckler & Koch se bazează de asemenea pe MG42 și multe alte modele Heckler & Koch, folosesc sistemul său de operare.</p>

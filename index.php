				<h3>Al Doilea Război Mondial</h3>
				<br>
				<p class="lead">Al Doilea Război Mondial a fost un război global care a durat din 1939 până în 1945, deși unele conflicte asociate lui au început și mai devreme. El a implicat marea majoritate a țărilor lumii—inclusiv toate marile puteri—care au format două alianțe militare opuse: Aliații și Axa. A fost cel mai întins război din istorie, și a implicat direct mai mult de 100 de milioane de oameni din peste 30 de țări. Cei mai importanți participanți și-au dedicat întreaga capabilitate economică, industrială și științifică efortului de război, într-o stare de „război total”, ștergând distincția între resursele civile și cele militare. </p>
				<img class="rounded float-right" src="public/img/pic_hitler.jpg" oncontextmenu="return false">
				<p class="lead">A inceput pe 1 septembrie 1939 prin atacul Germaniei asupra Poloniei.</p>
				<p class="lead">De la sfârșitul anului 1939 până la începutul anului 1941, într-o serie de campanii și tratate, Germania a cucerit sau controlat o mare parte din Europa continentală.
					Conform Pactului Molotov–Ribbentrop din 23 august 1939, Germania și Uniunea Sovietică au divizat anexat teritoriile vecinilor lor europeni, Polonia, Finlanda, România și Statele Baltice. Războiul a continuat în primul rând între puterile europene ale Axei și coaliția între Regatul Unit și Commonwealth-ul Britanic, cu campanii inclusiv în Africa de Nord⁠(d) și Africa de Est, cu bătălia aeriană a Marii Britanii, campania de bombardamente Blitz, campania din Balcani precum și cu îndelungata bătălie a Atlanticului. În iunie 1941, puterile europene ale Axei au lansat o invazie a Uniunii Sovietice, deschizând cel mai mare teatru de război din istorie, care a angajat cea mai mare parte a forțelor militare ale Axei într-un război de uzură. În decembrie 1941, Japonia a atacat Statele Unite ale Americii și coloniile europene⁠(d) din Oceanul Pacific, și a cucerit rapid o mare parte a Pacificului de Vest.
					Înaintarea Axei s-a oprit în 1942, când Japonia a pierdut critica bătălie de la Midway, în apropiere de Hawaii, iar Germania a fost învinsă în Africa de Nord și apoi, în mod decisiv, la Stalingrad, în Uniunea Sovietică. În 1943, după o serie de înfrângeri germane pe Frontul de Est, invaziile aliate a Siciliei și a Italiei care au condus la capitularea Italiei, și victorile aliate în Pacific, Axa a pierdut inițiativa și s-a angajat într-o retragere strategică pe toate fronturile. În 1944, Aliații au invadat Franța ocupată de Germania, în timp ce Uniunea Sovietică și-a recăpătat toate pierderile teritoriale și a invadat Germania și aliații săi. În 1944 și 1945, japonezii au suferit pierderi majore în Asia continentală în China Central-Sudică⁠(d), și Birmania⁠(d), în timp ce Aliații au schilodit Marina Japoneză și au capturat insule-cheie din Pacificul de Vest.
				Războiul din Europa s-a încheiat cu o invazie a Germaniei de către Aliații Occidentali și Uniunea Sovietică, care a culminat cu capturarea Berlinului de către trupele sovietice și ulterior capitularea necondiționată a Germaniei la 8 mai 1945. După Declarația de la Potsdam dată de către Aliați la 26 iulie 1945 și după refuzul Japoniei de a capitula în conformitate cu termenii acesteia, Statele Unite ale Americii au detonat bombe atomice deasupra orașelor japoneze Hiroshima și Nagasaki, la 6 și, respectiv, 9 august. Cum o invazie de arhipelagului Japonez⁠(d) era iminentă, și erau posibile și alte bombardamente atomice, iar Uniunea Sovietică a declarat război Japoniei⁠(d) și a invadat Manciuria, Japonia a capitulat și ea la 15 august 1945. Astfel s-a încheiat războiul în Asia, cimentând o victorie totală a Aliaților.</p>
				

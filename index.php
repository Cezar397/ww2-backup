				<p class="lead">Toate armatele mai mari foloseau aruncătoare de flăcări portabile pentru sarcini speciale, de exemplu în cazul luptei împotriva cazematelor. Toate erau foarte grele (aprox. 40 kg) și aveau rază limitată de acțiune (40-50m). Americanii l-au folosit la scară mare în luptele cu japonezii în insulele din Oceanul Pacific împotriva cazematelor, grotelor și galeriilor escavate.</p>

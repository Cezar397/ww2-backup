				<h2>Tancuri și artilerie</h2>
				<p class="lead">În august 1939 înainte de izbucnirea războiului în Europa, Gheorghi Jukov folosea forțe combinate de tancuri și aviație la Nomonhan în Mongolia împotriva armatei japoneze din Manciuria. În luptă au participat aproape 500 tancuri BT-5 și BT-7 de partea sovietică și 180 de tancuri japoneze. Aici Jukov a dezvoltat și încercat noi tehnici și tactici, folosite mai târziu pe Frontul de Est împotriva naziștilor. Aceste inovații includ construirea unor poduri sub apă (greu vizibile din avioane), îmbunătățirea coeziunii și eficacității unor unități neexperimentate prin adăugarea unor trupe experimentate care să le ridice moralul și instrucție generală. Practica a revelat unele neajunsuri ale tancurilor BT, experiență foarte valoroasă, care au condus la schimbarea motoarelor cu benzină ale acestor tancuri în motoare diesel, esențial în dezvoltarea ulterioară a tancului T-34.</p>
				<p class="lead">În 1939, la izbucnirea războiului în Europa, cel mai puternic tanc avea tunul principal de 37mm și blindaj de 40mm. În 1942 aceste date erau: tun de 75mm și blindaj de grosime dublă, cifre care în război au mai crescut. Tratatul de la Versailles a impus restricții severe Germaniei în privința construirii vehiculelor militare de-a lungul anilor 1920 și 1930. Producătorii germani de arme și Wehrmacht-ului au început dezvoltare în secret a tancurilor.</p>
				<p class="lead">Deoarece aceste vehicule au fost fabricate în secret, specificațiile tehnice ale acestora și potențialul lor pe câmpul de luptă au fost în mare parte necunoscute aliaților europeni până la începutul războiului.</p>
				<p class="lead">Atunci, când trupele germane au invadat națiunile Benelux și Franța în mai 1940, tehnologia militară germană, dar în special tactica și modul de folosire al acestor arme s-au dovedit a fi superioară celei a aliaților.</p>
				<p class="lead">În 1918, tancul Renault FT-17 al Franței era cel mai avansat tanc din lume. Deși mai mică, era capabilă să depășească omologii săi lenți și greoi britanici, germani sau americani. Generalii francezi și britanici credeau că într-un viitor război cu Germania ar fi luptat în condiții foarte similare cu cele din 1914-1918. Francezii au investit în tancuri cu blindaj gros, vehicule înarmate destinate să traverseze câmpuri de luptă devastate de bombe, șanțuri și să sprijine infanteria în lupta împotriva cuiburilor de mitraliere și fortificații sub foc inamic.</p>
				<h3>Artilerie autopropulsată</h3>
				<p class="lead">Doctrina Blitzkrieg impunea suport din partea artileriei, dar și viteză de deplasare a pieselor de artilerie. Astfel tractarea pieselor de artilerie cu cai sau alte mijloace a fost depășită și a apărut artileria autopropulsată. Germanii au fost prolifici în proiectare și foloseau o mare varietate de artilerie autopropulsată, ca de exemplu Wespe, Hummel, Grille, Panzerwerfer, Wurfrahmen 40, Thor, montate uneori pe șasiul unor tancuri ca de exemplu Jagdpanzer IV, Jagdpanther și Elefant.</p>
				<h3>Tunuri de asalt</h3>
				
				<p class="lead">În timpul celui de-al Doilea Război Mondial Germania nazistă a dezvoltat mai multe tunuri de asalt pentru sprijinirea infanteriei prin foc direct asupra trupelor și fortificațiilor inamice echipate cu un tun normal și un obuzier. Aici se pot aminti StuG III, StuG IV, Sturmpanzer IV (Sd.Kfz. 166), Sturmtiger.<br>
					Armata Roșie a folosit tunuri de asalt SU-76 montat pe șasiul modificat al tancului ușor T-70, SU-76i, SU-122, SU-152, ISU-122 și ISU-152.<br>
					Marea Britanie și Statele Unite ale Americii foloseau tancuri dotate cu obuziere în loc de tunul normal ca de exemplu varianta M4(105) a tancului Sherman înarmată cu un obuzier de 105 mm.<br>
